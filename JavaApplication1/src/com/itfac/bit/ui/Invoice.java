    private void cmb_orderNumberItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_orderNumberItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            txt_customerID.setText("");
            txt_firstName.setText("");
            txt_lastName.setText("");
            txt_addressNumber.setText("");
            txt_addressStreet.setText("");
            txt_addressCity.setText("");
            txt_deliveryAddressNumber.setText("");
            txt_deliveryAddressStreet.setText("");
            txt_deliveryAddressCity.setText("");
            txt_deliveryAddressDistrict.setText("");
            txt_contactNumber.setText("");
            txt_email.setText("");
            txt_faxNo.setText("");
            txt_orderTotal.setText("");
            txt_subTotal.setText("");
            txt_deliveryCharges.setText("");
            txt_grandTotal.setText("");
            modelo.setRowCount(0);
            ba = String.valueOf(cmb_orderNumber.getSelectedItem());

            try {
                st5 = con.createStatement();
                rs5 = st5.executeQuery("SELECT * FROM orders_header WHERE ord_No = '" + ba + "'");

                while (rs5.next()) {
                    String cid = rs5.getString(7);
                    st4 = con.createStatement();
                    rs4 = st4.executeQuery("SELECT * FROM customer_master WHERE cus_ID = '" + cid + "'");

                    while (rs4.next()) {
                        txt_customerID.setText(rs4.getString(1));
                        txt_firstName.setText(rs4.getString(2));
                        txt_lastName.setText(rs4.getString(3));
                        txt_addressNumber.setText(rs4.getString(4));
                        txt_addressStreet.setText(rs4.getString(5));
                        txt_addressCity.setText(rs4.getString(6));
                        txt_deliveryAddressNumber.setText(rs4.getString(7));
                        txt_deliveryAddressStreet.setText(rs4.getString(8));
                        txt_deliveryAddressCity.setText(rs4.getString(9));
                        txt_deliveryAddressDistrict.setText(rs4.getString(10));
                        txt_contactNumber.setText(String.valueOf(rs4.getInt(12)));
                        txt_email.setText(rs4.getString(11));
                        txt_faxNo.setText(String.valueOf(rs4.getInt(13)));
                    }

                    st6 = con.createStatement();
                    rs6 = st6.executeQuery("SELECT * FROM orders_details WHERE ord_No = '" + ba + "'");

                    String in = "", up = "", it = "";
                    Double upp = 0.0, ito = 0.0;
                    while (rs6.next()) {
                        String ic = rs6.getString(2);
                        st7 = con.createStatement();
                        rs7 = st7.executeQuery("SELECT * FROM item_master WHERE item_Code = '" + ic + "'");
                        if (rs7.next()) {
                            in = rs7.getString(2);
                            upp = rs7.getDouble(4);
                            up = String.valueOf(upp);
                        }

                        int qty = rs6.getInt(3);
                        ito = qty * upp;
                        it = String.valueOf(ito);
                        modelo.addRow(new Object[]{ic, in, String.valueOf(qty), up, it});
                    }

                    Double oto = rs5.getDouble(3);
                    Double gto = rs5.getDouble(4);
                    Double dch = gto - oto;

                    txt_orderTotal.setText(String.valueOf(oto));
                    txt_subTotal.setText(String.valueOf(oto));
                    txt_deliveryCharges.setText(String.valueOf(dch));
                    txt_grandTotal.setText(String.valueOf(gto));
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_cmb_orderNumberItemStateChanged

    private void btn_generateInvoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generateInvoiceActionPerformed
        String inv = txt_invoiceNumber.getText();
        String dat = txt_date.getText();
        String tim = dateFormat2.format(cal2.getTime());
        Double tot = Double.parseDouble(txt_grandTotal.getText());
        String note = txt_notes.getText();
        String ordn = String.valueOf(cmb_orderNumber.getSelectedItem());
        String delReq = "", ordStatus = "";
        
        try {
            
            st10 = con.createStatement();
            rs10 = st10.executeQuery("SELECT del_Req FROM orders_header WHERE ord_No = '" + ordn + "'");
            
            while(rs10.next()) {
                delReq = rs10.getString(1);
            }
            
            if(delReq.equals("Yes")) {
                ordStatus = "Invoice Generated";
            } else {
                ordStatus = "Completed";
            }
            
            st8 = con.createStatement();
            st8.executeUpdate("INSERT INTO invoice_master VALUES('" + inv + "','" + dat + "','" + tim + "','" + tot + "','" + note + "','" + ordn + "')");
            st9 = con.createStatement();
            st9.executeUpdate("UPDATE orders_header SET ord_Status = '" + ordStatus + "' WHERE ord_No = '" + ordn + "'");
            JOptionPane.showMessageDialog(this, "Invoice Successfully Generated", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
            createInvoicePDF();
            mmm.getInvData();
            mmm.getOrdData();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        }

    }//GEN-LAST:event_btn_generateInvoiceActionPerformed

