    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        try {
            
            statement = connection.prepareStatement("UPDATE delivery_representative_master SET dRep_FName=?,dRep_LName=?,dRep_Email=?,dRep_Tel=?,dRep_Type=? WHERE emp_ID=?");
            
            
            DeliveryRepresentator  deliveryRepresentator = new DeliveryRepresentator();
            
            deliveryRepresentator.setEmployeeID(txt_employeeID.getText());
            deliveryRepresentator.setFirstName(txt_firstName.getText());
            deliveryRepresentator.setLastName(txt_lastName.getText());
            deliveryRepresentator.setTelephoneNumber(Integer.parseInt(txt_contactNumber.getText()));
            deliveryRepresentator.setEmail(txt_email.getText());
            
            
            if(String.valueOf(cmb_type.getSelectedItem()).equals("Driver")){
                deliveryRepresentator.setDeliveryRepresentatorType(DeliveryRepresentatorType.DRIVER);
            }else{
                deliveryRepresentator.setDeliveryRepresentatorType(DeliveryRepresentatorType.SALES_REPRESENTATIVE);
            }
            
            
            
            if(deliveryRepresentator.getFirstName().isEmpty() || deliveryRepresentator.getLastName().isEmpty() 
                    || deliveryRepresentator.getEmail().isEmpty() ||
                     deliveryRepresentator.getEmployeeID().isEmpty() || deliveryRepresentator.getTelephoneNumber() <= 0){
                JOptionPane.showMessageDialog(this, "All fields required.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isNameValid(deliveryRepresentator.getFirstName())){
                JOptionPane.showMessageDialog(this, "Error in First Name. First Name should contain only Alphabetic Characters.\nExample:- Kamal", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isNameValid(deliveryRepresentator.getLastName())){
                JOptionPane.showMessageDialog(this, "Error in First Name. Last Name should contain only Alphabetic Characters.\nExample:- Kamal", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isEmailvalid(deliveryRepresentator.getEmail())){
                JOptionPane.showMessageDialog(this, "Error in Email. Email should contain be in following format.\nExample:- kamal@gmail.com ", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isContactNumberValid(txt_contactNumber.getText())){
                JOptionPane.showMessageDialog(this, "Error in Contact Number. Contact Number should contain only Numerical Characters.\nExample:- 0777456123", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else{
                
                statement.setString(1, deliveryRepresentator.getFirstName());
                statement.setString(2, deliveryRepresentator.getLastName());
                statement.setString(3, deliveryRepresentator.getEmail());
                statement.setInt(4, deliveryRepresentator.getTelephoneNumber());
                statement.setString(5, deliveryRepresentator.getDeliveryRepresentatorType().toString());
                statement.setString(6, deliveryRepresentator.getEmployeeID());
                
                if(statement.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(this, "Update Successful", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    mmm.getDelData();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Update failed.", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

