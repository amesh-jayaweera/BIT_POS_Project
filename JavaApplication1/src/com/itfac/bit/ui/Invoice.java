    private void createInvoicePDF() {
        Document document = new Document();
        try {
            String invNo = txt_invoiceNumber.getText();
            PdfWriter writer = PdfWriter.getInstance(document, new FileOutputStream("Invoice-" + invNo + ".pdf"));
            document.open();

            //Add Image
            Image image1 = Image.getInstance(System.getProperty("user.dir") + "\\src\\com\\itfac\\bit\\companylogo.jpg");
            //Scale to new height and new width of image
            image1.scaleAbsolute(200, 100);
            //Add to document
            document.add(image1);

            Font blueFont = FontFactory.getFont(FontFactory.COURIER, 30, Font.BOLD, new CMYKColor(255, 0, 0, 0));
            document.add(new Paragraph("Invoice", blueFont));

            LineSeparator ls = new LineSeparator();
            document.add(new Chunk(ls));

            String cusID = txt_customerID.getText();
            String cusName = txt_firstName.getText() + " " + txt_lastName.getText();
            String address = txt_addressNumber.getText() + ", " + txt_addressStreet.getText() + ", " + txt_addressCity.getText();
            String delAddress = txt_deliveryAddressNumber.getText() + ", " + txt_deliveryAddressStreet.getText() + ", " + txt_deliveryAddressCity.getText() + ", " + txt_deliveryAddressDistrict.getText();
            String contact = txt_contactNumber.getText();
            String email = txt_email.getText();
            String fax = txt_faxNo.getText();
            document.add(new Paragraph("Customer ID : " + cusID));
            document.add(new Paragraph("Customer Name : " + cusName));
            document.add(new Paragraph("Address : " + address));
            document.add(new Paragraph("Delivery Address : " + delAddress));
            document.add(new Paragraph("Contact Number : " + contact));
            document.add(new Paragraph("Email : " + email));
            document.add(new Paragraph("Fax : " + fax));

            PdfPTable table = new PdfPTable(5); // 5 columns.
            table.setWidthPercentage(100); //Width 100%
            table.setSpacingBefore(20f); //Space before table
            table.setSpacingAfter(20f); //Space after table

            //Set Column widths
            float[] columnWidths = {1f, 1f, 1f, 1f, 1f};
            table.setWidths(columnWidths);

            PdfPCell cell1 = new PdfPCell(new Paragraph("Item Code"));
            cell1.setPaddingLeft(10);
            cell1.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell1.setVerticalAlignment(Element.ALIGN_LEFT);
            cell1.setFixedHeight(30f);

            PdfPCell cell2 = new PdfPCell(new Paragraph("Item Name"));
            cell2.setPaddingLeft(10);
            cell2.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell2.setVerticalAlignment(Element.ALIGN_LEFT);
            cell2.setFixedHeight(30f);

            PdfPCell cell3 = new PdfPCell(new Paragraph("Quantity"));
            cell3.setPaddingLeft(10);
            cell3.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell3.setVerticalAlignment(Element.ALIGN_LEFT);
            cell3.setFixedHeight(30f);

            PdfPCell cell4 = new PdfPCell(new Paragraph("Unit Price"));
            cell4.setPaddingLeft(10);
            cell4.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell4.setVerticalAlignment(Element.ALIGN_LEFT);
            cell4.setFixedHeight(30f);

            PdfPCell cell5 = new PdfPCell(new Paragraph("Item Total"));
            cell5.setPaddingLeft(10);
            cell5.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell5.setVerticalAlignment(Element.ALIGN_LEFT);
            cell5.setFixedHeight(30f);
            
            table.addCell(cell1);
            table.addCell(cell2);
            table.addCell(cell3);
            table.addCell(cell4);
            table.addCell(cell5);

            int rows = table_invoice.getRowCount();

            for (int i = 0; i < rows; i++) {
                String aa = String.valueOf(table_invoice.getValueAt(i, 0));
                String ab = String.valueOf(table_invoice.getValueAt(i, 1));
                String ac = String.valueOf(table_invoice.getValueAt(i, 2));
                String ad = String.valueOf(table_invoice.getValueAt(i, 3));
                String ae = String.valueOf(table_invoice.getValueAt(i, 4));

                PdfPCell cell6 = new PdfPCell(new Paragraph(aa));
                cell6.setPaddingLeft(10);
                cell6.setHorizontalAlignment(Element.ALIGN_LEFT);
                cell6.setVerticalAlignment(Element.ALIGN_LEFT);
                cell6.setFixedHeight(30f);

                PdfPCell cell7 = new PdfPCell(new Paragraph(ab));
                cell7.setPaddingLeft(10);
                cell7.setHorizontalAlignment(Element.ALIGN_LEFT);
                cell7.setVerticalAlignment(Element.ALIGN_LEFT);
                cell7.setFixedHeight(30f);
                
                PdfPCell cell8 = new PdfPCell(new Paragraph(ac));
                cell8.setPaddingLeft(10);
                cell8.setHorizontalAlignment(Element.ALIGN_LEFT);
                cell8.setVerticalAlignment(Element.ALIGN_LEFT);
                cell8.setFixedHeight(30f);

                PdfPCell cell9 = new PdfPCell(new Paragraph(ad));
                cell4.setPaddingLeft(10);
                cell4.setHorizontalAlignment(Element.ALIGN_LEFT);
                cell4.setVerticalAlignment(Element.ALIGN_LEFT);

                PdfPCell cell10 = new PdfPCell(new Paragraph(ae));
                cell10.setPaddingLeft(10);
                cell10.setHorizontalAlignment(Element.ALIGN_LEFT);
                cell10.setVerticalAlignment(Element.ALIGN_LEFT);
                cell10.setFixedHeight(30f);
                
                table.addCell(cell6);
                table.addCell(cell7);
                table.addCell(cell8);
                table.addCell(cell9);
                table.addCell(cell10);
            }

            document.add(table);

            String notes = txt_notes.getText();
            String subTotal = txt_subTotal.getText();
            String delCharge = txt_deliveryCharges.getText();
            String grandTotal = txt_grandTotal.getText();

            Paragraph p1 = new Paragraph("Invoice Notes : " + notes);
            p1.setSpacingBefore(20f);
            p1.setSpacingAfter(20f);
            document.add(p1);

            document.add(new Paragraph("Sub Total        : " + subTotal));
            document.add(new Paragraph("Delivery Charges : " + delCharge));
            document.add(new Paragraph("Grand Total      : " + grandTotal));

            document.close();
            writer.close();
            Desktop.getDesktop().open(new File("Invoice-" + invNo + ".pdf"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        }
    }

