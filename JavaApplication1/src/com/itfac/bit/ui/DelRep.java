    public DelRep(MainNew mainNew) {
        
        this.mainNew = mainNew;
        initComponents();
        this.setResizable(false); 
        this.setLocationRelativeTo(null);
        try {
            connection = Database.openConnection();
            
            preparedStatement = connection.prepareStatement("SELECT emp_ID FROM delivery_representative_master");
            
            resultSet = preparedStatement.executeQuery();

            if (!resultSet.isBeforeFirst()) {
                format = String.format("%06d", 1);
            } else {
                resultSet.last();
                int empID = resultSet.getInt(1);
                empID = empID + 1;
                format = String.format("%06d", empID);
            }

            txt_id.setText(format);
            txt_id.setEditable(false);
            cmb_type.addItem("Sales Representative");
            cmb_type.addItem("Driver");
            cmb_type.addItem("Main");
            cmb_type.setSelectedIndex(-1);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
