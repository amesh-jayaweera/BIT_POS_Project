    @SuppressWarnings("unchecked")
    public CustomerUI(MainNew mainMenu) {
        
        this.mainMenu = mainMenu;
        initComponents();
        this.setResizable(false); 
        this.setLocationRelativeTo(null);
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        Calendar cal = Calendar.getInstance();
        txt_date.setText(dateFormat.format(cal.getTime()));
        txt_date.setEditable(false);

        this.customer = new Customer();
        
        try {
            
            connection = Database.openConnection();

            statement = connection.createStatement();
            reusltSet = statement.executeQuery("SELECT cus_ID FROM customer_master");
            
            
            if(reusltSet.next() == false){
                this.customer.setId("1");
            }else{
                reusltSet.last();
                this.customer.setId(Integer.toString(reusltSet.getInt(1) + 1));
            }
            
            
            
            txt_customerID.setText(String.format("%06d", Integer.parseInt(this.customer.getId())));
            txt_customerID.setEditable(false);

            statement = connection.createStatement();
            reusltSet = statement.executeQuery("SELECT district FROM delivery_charges_master");

            while (reusltSet.next()) {
                this.customer.setDeliveryAddressDistrict(reusltSet.getString(1));
                cmb_deliveryAddressDistrict.addItem(this.customer.getDeliveryAddressDistrict());
            }
            cmb_deliveryAddressDistrict.setSelectedIndex(-1);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }

