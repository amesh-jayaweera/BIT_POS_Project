    public DelRepUpdate(MainNew mm, String delRepID) {
        this.setResizable(false); 
        this.setLocationRelativeTo(null);
        mmm = mm;
        initComponents();
        try {
            connection = Database.openConnection();

            statement = connection.prepareStatement("SELECT * FROM delivery_representative_master WHERE emp_ID=?");
            statement.setString(1, delRepID);
            resultSet = statement.executeQuery();
            
            txt_employeeID.setText(delRepID);

            cmb_type.addItem("Driver");
            cmb_type.addItem("Sales Representative");
            cmb_type.addItem("Main");
            


            while (resultSet.next()) {
                txt_firstName.setText(resultSet.getString(2));
                txt_lastName.setText(resultSet.getString(3));
                
            if(resultSet.getString(6).equals(DeliveryRepresentatorType.SALES_REPRESENTATIVE.toString())){
                cmb_type.setSelectedItem("Sales Representative");
            }else if(resultSet.getString(6).equals(DeliveryRepresentatorType.DRIVER.toString())){
                cmb_type.setSelectedItem("Driver");
            }else if(resultSet.getString(6).equals(DeliveryRepresentatorType.MAIN.toString())){
                cmb_type.setSelectedItem("Main");
            }
                
               
                txt_contactNumber.setText("0"+String.valueOf(resultSet.getInt(5)));
                txt_email.setText(resultSet.getString(4));

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        }
    }

