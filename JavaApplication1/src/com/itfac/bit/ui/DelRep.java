    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        try {
           
            
            DeliveryRepresentator deliveryRepresentator =  new DeliveryRepresentator();
            
            deliveryRepresentator.setEmployeeID(txt_id.getText());
            deliveryRepresentator.setFirstName(txt_firstName.getText());
            deliveryRepresentator.setLastName(txt_lastName.getText());
            deliveryRepresentator.setEmail(txt_email.getText());
      
            if(!Validation.isContactNumberValid(txt_contactNumber.getText())){
                JOptionPane.showMessageDialog(this, "Error in Contact Number. Contact Number should contain only 10. \nExample:- 0777456123", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else{
                deliveryRepresentator.setTelephoneNumber(Integer.parseInt(txt_contactNumber.getText()));
                
                deliveryRepresentator.setDeliveryRepresentatorStatus(DeliveryRepresentatorStatus.AVAILABLE);
            
            if(String.valueOf(cmb_type.getSelectedItem()).equals("Sales Representative")){
                deliveryRepresentator.setDeliveryRepresentatorType(DeliveryRepresentatorType.SALES_REPRESENTATIVE); 
            }else if(String.valueOf(cmb_type.getSelectedItem()).equals("Driver")){
                 deliveryRepresentator.setDeliveryRepresentatorType(DeliveryRepresentatorType.DRIVER); 
            }else if(String.valueOf(cmb_type.getSelectedItem()).equals("Main")){
                 deliveryRepresentator.setDeliveryRepresentatorType(DeliveryRepresentatorType.MAIN); 
            }
            
            if(deliveryRepresentator.getFirstName().isEmpty() ||
                    deliveryRepresentator.getLastName().isEmpty() || deliveryRepresentator.getEmployeeID().isEmpty()
                    || deliveryRepresentator.getEmail().isEmpty() ||
                    deliveryRepresentator.getTelephoneNumber() == 0){
                 JOptionPane.showMessageDialog(this, "All fields required", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isNameValid(deliveryRepresentator.getFirstName())){
                JOptionPane.showMessageDialog(this, "Error in First Name. First Name should contain only Alphabetic Characters.\nExample:- Kamal", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isNameValid(deliveryRepresentator.getLastName())){
                JOptionPane.showMessageDialog(this, "Error in First Name. Last Name should contain only Alphabetic Characters.\nExample:- Kamal", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(!Validation.isEmailvalid(deliveryRepresentator.getEmail())){
                JOptionPane.showMessageDialog(this, "Error in Email. Email should contain be in following format.\nExample:- kamal@gmail.com ", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            
            else{
               preparedStatement = connection.prepareStatement("INSERT INTO delivery_representative_master VALUES(?,?,?,?,?,?,?)");
               preparedStatement.setString(1,deliveryRepresentator.getEmployeeID() );
               preparedStatement.setString(2, deliveryRepresentator.getFirstName());
               preparedStatement.setString(3, deliveryRepresentator.getLastName());
               preparedStatement.setString(4, deliveryRepresentator.getEmail());
               preparedStatement.setInt(5, deliveryRepresentator.getTelephoneNumber());
               preparedStatement.setString(6, deliveryRepresentator.getDeliveryRepresentatorType().toString());
               preparedStatement.setString(7, "Available");
               
                if(preparedStatement.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(this, "Submit Successful", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    mainNew.getDelData();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Registration failed", "ERROR", JOptionPane.ERROR_MESSAGE);
                }

            }
            }
         
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btn_submitActionPerformed

