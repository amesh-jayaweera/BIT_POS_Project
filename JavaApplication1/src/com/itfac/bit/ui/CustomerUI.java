    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
        try {
            statement = connection.createStatement();
            
            boolean entriesCorrect = false;
            
            if(txt_firstName.getText().isEmpty() || txt_lastName.getText().isEmpty() 
                    || txt_addressNumber.getText().isEmpty() || txt_addressStreet.getText().isEmpty() 
                    || txt_addressCity.getText().isEmpty() || txt_contactNumber.getText().isEmpty() 
                    || txt_email.getText().isEmpty() || txt_deliverAddressNumber.getText().isEmpty() 
                    || txt_deliveryAddressStreet.getText().isEmpty() || txt_deliveryAddressCity.getText().isEmpty()
                    || txt_fax.getText().isEmpty() || txt_customerID.getText().isEmpty() || String.valueOf(cmb_deliveryAddressDistrict.getSelectedItem()).isEmpty()){
                
                    JOptionPane.showMessageDialog(this, "Please fill all fields", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else{
                
                
                this.customer.setFirstName(txt_firstName.getText());
                this.customer.setLastName(txt_lastName.getText());
                this.customer.setAddressNumber(txt_addressNumber.getText());
                this.customer.setAddressStreet(txt_addressStreet.getText());
                this.customer.setAddressCity(txt_addressCity.getText());
                this.customer.setEmail(txt_email.getText());
                this.customer.setDeliveryAddressNumber(txt_deliverAddressNumber.getText());
                this.customer.setDeliveryAddressStreet(txt_deliveryAddressStreet.getText());
                this.customer.setDeliveryAddressCity(txt_deliveryAddressCity.getText());
                
                
                if(!Validation.isNumberValid(txt_fax.getText())){
                    JOptionPane.showMessageDialog(this, "Error in Fax No., Fax No. should contain only digits", "ERROR", JOptionPane.ERROR_MESSAGE);
                }else if(!Validation.isNumberValid(txt_contactNumber.getText())){
                    JOptionPane.showMessageDialog(this, "Error in Contact No., Contact No. should contain only digits", "ERROR", JOptionPane.ERROR_MESSAGE);
                }else if(!Validation.isContactNumberValid(txt_contactNumber.getText())){
                    JOptionPane.showMessageDialog(this, "Error in Contact No., Contact No. should contain only 10 digits", "ERROR", JOptionPane.ERROR_MESSAGE);
                }else {
                    this.customer.setFaxNo(Integer.parseInt(txt_fax.getText()));
                    this.customer.setTelephoneNumber(Integer.parseInt(txt_contactNumber.getText()));
                
                
                      
                this.customer.setId(txt_customerID.getText());
    //           this.customer.setRegistrationDate(txt_date.getText());
                this.customer.setDeliveryAddressDistrict(String.valueOf(cmb_deliveryAddressDistrict.getSelectedItem()));
                
                
                
                if (!Validation.isNameValid(this.customer.getFirstName())) {
                    JOptionPane.showMessageDialog(this, "Error in First Name. First Name should contain only Alphabetic Characters.\nExample:- Kamal", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isNameValid(this.customer.getLastName())) {
                    JOptionPane.showMessageDialog(this, "Error in Last Name. Last Name should contain only Alphabetic Characters.\nExample:- Perera", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isNumberValid(this.customer.getAddressNumber())) {
                    JOptionPane.showMessageDialog(this, "Error in Address Number. Address Number should contain only Numerical Characters.\nExample:- 32", "ERROR", JOptionPane.ERROR_MESSAGE);
                }  else if (!Validation.isNameValid(this.customer.getAddressStreet())) {
                    JOptionPane.showMessageDialog(this, "Error in Address Street. Address Street should contain only Alphabetic Characters.\nExample:- Galle Road", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isNameValid(this.customer.getAddressCity())) {
                    JOptionPane.showMessageDialog(this, "Error in Address City. Address City should contain only Alphabetic Characters.\nExample:- Fort ", "ERROR", JOptionPane.ERROR_MESSAGE);
                }  else if (!Validation.isEmailvalid(this.customer.getEmail())) {
                    JOptionPane.showMessageDialog(this, "Error in Email. Email should contain be in following format.\nExample:- kamal@gmail.com ", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isNumberValid(this.customer.getDeliveryAddressNumber())) {
                    JOptionPane.showMessageDialog(this, "Error in Address Number. Address Number should contain only Numerical Characters.\nExample:- 32", "ERROR", JOptionPane.ERROR_MESSAGE);
                }  else if (!Validation.isNameValid(this.customer.getDeliveryAddressStreet())) {
                    JOptionPane.showMessageDialog(this, "Error in Address Street. Address Street should contain only Alphabetic Characters.\nExample:- Galle Road", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isNameValid(this.customer.getDeliveryAddressCity())) {
                    JOptionPane.showMessageDialog(this, "Error in Address City. Address City should contain only Alphabetic Characters.\nExample:- Fort ", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (!Validation.isContactNumberValid(txt_contactNumber.getText())) {
                    JOptionPane.showMessageDialog(this, "Error in Contact Number. Contact Number should contain only Numerical Characters.\nExample:- 0777456123", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (cmb_deliveryAddressDistrict.getSelectedIndex() == -1) {
                    JOptionPane.showMessageDialog(this, "Error in Address District. Please Select an Item from the ComboBox Menu.\n", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    entriesCorrect = true;
                }
                
                if (entriesCorrect) {
                    
                    preparedStatement = connection.prepareStatement("INSERT INTO customer_master VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
                    preparedStatement.setString(1, this.customer.getId());
                    preparedStatement.setString(2, this.customer.getFirstName());
                    preparedStatement.setString(3, this.customer.getLastName());
                    preparedStatement.setString(4, this.customer.getAddressNumber());
                    preparedStatement.setString(5, this.customer.getAddressStreet());
                    preparedStatement.setString(6, this.customer.getAddressCity());
                    preparedStatement.setString(7, this.customer.getDeliveryAddressNumber());
                    preparedStatement.setString(8, this.customer.getDeliveryAddressStreet());
                    preparedStatement.setString(9, this.customer.getDeliveryAddressCity());
                    preparedStatement.setString(10, this.customer.getDeliveryAddressDistrict());
                    preparedStatement.setString(11, this.customer.getEmail());
                    preparedStatement.setInt(12, this.customer.getTelephoneNumber());
                    preparedStatement.setInt(13,this.customer.getFaxNo());
                    preparedStatement.setString(14,txt_date.getText());
                    
                    preparedStatement.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Submit Successful", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    this.mainMenu.getCusData();
                    this.dispose();
                }    
        }
            
                
                
                
                }
                
              
                 
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btn_registerActionPerformed

