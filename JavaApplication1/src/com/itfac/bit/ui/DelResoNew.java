    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        String delNo = txt_deliveryNumber.getText();
        String delDate = txt_date.getText();
        String status = "Pending";
        String generatedStatus = "Not Generated";
        String query2 = "";
        

        if (!lm.isEmpty()) {
            if (table_employee.getRowCount() != 0) {
                if (table_vehicle.getRowCount() != 0) {
                    try {
                      
                        statement = connection.prepareStatement("INSERT INTO delivery_header VALUES(?,?,?,?)");
                        statement.setString(1, delNo);
                        statement.setString(2, delDate);
                        statement.setString(3, status);
                        statement.setString(4, generatedStatus);
                        
                        int res = statement.executeUpdate();
                       
                        
                        int lno = lm.getSize();

                        for (int k = 0; k < lno; k++) {
                            String orderNo = String.valueOf(lm.getElementAt(k));
                            
  
                            statement = connection.prepareStatement("UPDATE orders_header SET ord_Status = 'Delivery Assigned',del_No=? WHERE ord_No=?");
                            statement.setString(1, delNo);
                            statement.setString(2,orderNo);
                            res = statement.executeUpdate();
                            
                        }

                        int rows = table_employee.getRowCount();

                        for (int i = 0; i < rows; i++) {
                            String employeeID = String.valueOf(table_employee.getValueAt(i, 0));
                            String ee = String.valueOf(table_employee.getValueAt(i, 3));
                           
                            
                            statement = connection.prepareStatement("INSERT INTO delivery_and_delivery_representative_detail VALUES(?,?)");
                            statement.setString(1, delNo);
                            statement.setString(2,employeeID);
                            res = statement.executeUpdate();
                            
                            
                            statement = connection.prepareStatement("UPDATE delivery_representative_master SET dRep_Status = 'Not Available' WHERE emp_ID=?");
                            statement.setString(1, employeeID);
                            res = statement.executeUpdate();
                        
                        }

                        int rows2 = table_vehicle.getRowCount();

                        for (int i = 0; i < rows2; i++) {
                            String vehicleID = String.valueOf(table_vehicle.getValueAt(i, 0));
                           
                          
                            statement = connection.prepareStatement("INSERT INTO delivery_and_vehicle_detail VALUES(?,?)");
                            statement.setString(1, delNo);
                            statement.setString(2,vehicleID);
                            res = statement.executeUpdate();
                           
                            
                            statement = connection.prepareStatement("UPDATE vehicle_master SET veh_Status = 'Not Available' WHERE veh_No=?");
                            statement.setString(1, vehicleID);
                            res = statement.executeUpdate();
                        
                        
                        }

                        JOptionPane.showMessageDialog(this, "Delivery Resources Successfully Assigned", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                        mainNew.getDeliveryData();
                        mainNew.getDelData();
                        mainNew.getVehData();
                        mainNew.getOrdData();
                        this.dispose();
                    } catch (HeadlessException | SQLException e) {
                        JOptionPane.showMessageDialog(this, "Error Occured.\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                        System.out.println(e);
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "No Vehicles Selected", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No Delivery Representatives Selected", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No Orders Selected", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private void btn_clearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_clearActionPerformed
        txt_firstName.setText("");
        txt_lastName.setText("");
        txt_contactNumber.setText("");
        cmb_employeeType.setText("");
        txt_brand.setText("");
        txt_model.setText("");
        txt_vehicleType.setText("");
        
        int rows = table_employee.getRowCount();
        for (int i = 0; i < rows; i++) {
            String empID = String.valueOf(table_employee.getValueAt(i, 0));
            cmb_employeeID.addItem(empID);
        }
        
        rows = table_vehicle.getRowCount();
        for (int i = 0; i < rows; i++) {
            String vehNo = String.valueOf(table_vehicle.getValueAt(i, 0));
            cmb_vehicleNumber.addItem(vehNo);
        }
        
        tableModelEmployee.setRowCount(0);
        tableModelVehicle.setRowCount(0);
        
        while(!lm.isEmpty()) {
            String orderNo = lm.get(0).toString();
            cmb_orderNumber.addItem(orderNo);
            lm.remove(0);
        }
        
        cmb_orderNumber.setSelectedIndex(-1);
        cmb_employeeID.setSelectedIndex(-1);
        cmb_vehicleNumber.setSelectedIndex(-1);
    }//GEN-LAST:event_btn_clearActionPerformed

