    public DelResoNew(MainNew mainNew) {
        this.setResizable(false); 
        this.setLocationRelativeTo(null);
        this.mainNew = mainNew;
        initComponents();
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        Calendar cal = Calendar.getInstance();
        txt_date.setText(dateFormat.format(cal.getTime()));
        txt_date.setEditable(false);
        lm = new DefaultListModel();
        tableModelEmployee = (DefaultTableModel) table_employee.getModel();
        tableModelVehicle = (DefaultTableModel) table_vehicle.getModel();

        try {
            connection = Database.openConnection();
         
            statement = connection.prepareStatement("SELECT del_No FROM delivery_header");
            resultSet = statement.executeQuery();

            if (!resultSet.isBeforeFirst()) {
                format = String.format("%06d", 1);
            } else {
                resultSet.last();
                int delNo = resultSet.getInt(1);

                delNo = delNo + 1;
                format = String.format("%06d", delNo);
            }

            txt_deliveryNumber.setText(format);
            txt_deliveryNumber.setEditable(false);

            statement = connection.prepareStatement("SELECT ord_No FROM orders_header WHERE ord_Status='Placed' AND del_Req='Yes'");
            resultSet1 = statement.executeQuery();

            while (resultSet1.next()) {
                cmb_orderNumber.addItem(resultSet1.getString(1));
            }

            
            statement = connection.prepareStatement("SELECT emp_ID FROM delivery_representative_master WHERE dRep_Status='Available'");
            resultSet2 = statement.executeQuery();
            
            while (resultSet2.next()) {
                cmb_employeeID.addItem(resultSet2.getString(1));
            }

                
            statement = connection.prepareStatement("SELECT veh_No FROM vehicle_master WHERE veh_Status='Available'");
            resultSet3 = statement.executeQuery();
            
            while (resultSet3.next()) {
                cmb_vehicleNumber.addItem(resultSet3.getString(1));
            }

            cmb_orderNumber.setSelectedIndex(-1);
            cmb_employeeID.setSelectedIndex(-1);
            cmb_vehicleNumber.setSelectedIndex(-1);
            btn_removeOrderNumber.setEnabled(false);

            txt_firstName.setText("");
            txt_lastName.setText("");
            txt_contactNumber.setText("");
            cmb_employeeType.setText("");
            txt_brand.setText("");
            txt_model.setText("");
            txt_vehicleType.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }

