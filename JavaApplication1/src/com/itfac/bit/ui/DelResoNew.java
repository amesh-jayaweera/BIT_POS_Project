    private void btn_addOrderNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addOrderNumberActionPerformed
        if(cmb_orderNumber.getSelectedIndex() != -1) {
            String ordNo = String.valueOf(cmb_orderNumber.getSelectedItem());
            cmb_orderNumber.removeItem(ordNo);
            cmb_orderNumber.setSelectedIndex(-1);
            lm.addElement(ordNo);
            jList1.setModel(lm);
            int size = lm.getSize();

            if (size != 0) { //Nobody's left, disable firing.
                btn_removeOrderNumber.setEnabled(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Select an Order Number", "ERROR", JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_btn_addOrderNumberActionPerformed

    private void btn_removeOrderNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeOrderNumberActionPerformed
        if (!jList1.isSelectionEmpty()) {
            String ordd = String.valueOf(jList1.getSelectedValue());
            int index = jList1.getSelectedIndex();
            lm.remove(index);
            cmb_orderNumber.addItem(ordd);
            int size = lm.getSize();

            if (size == 0) { //Nobody's left, disable firing.
                btn_removeOrderNumber.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Select an Order Number from the List", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_removeOrderNumberActionPerformed

    private void cmb_employeeIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_employeeIDItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            String eID = String.valueOf(cmb_employeeID.getSelectedItem());

            try {
               
                statement = connection.prepareStatement("SELECT * FROM delivery_representative_master WHERE emp_ID=?");
                statement.setString(1, eID);
                resultSet = statement.executeQuery();
                
                if (resultSet.next()) {
                    txt_firstName.setText(resultSet.getString(2));
                    txt_lastName.setText(resultSet.getString(3));
                    txt_contactNumber.setText(String.valueOf(resultSet.getInt(5)));
                    cmb_employeeType.setText(resultSet.getString(6));

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error Occured.\n" + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_cmb_employeeIDItemStateChanged

